    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         try {
        // R√©cup√©ration des donn√©es depuis les champs
        int idsortie = Integer.parseInt(txtidsortie.getText().trim());
        String motif = txtmotif.getText().trim();
        int montant = Integer.parseInt(txtmontantsortie.getText().trim());

        // V√©rifie si une date a √©t√© choisie
        java.util.Date dateUtil = datesortie.getDate();
        if (dateUtil == null) {
            Methodes.showError("Veuillez choisir une date.");
            return;
        }
        java.sql.Date dateSQL = new java.sql.Date(dateUtil.getTime());

        // ‚úÖ Connexion √† la base via ta m√©thode existante
        Connection conn = Methodes.getconnexion(); // üëà Remplace null par ta vraie m√©thode
        if (conn == null) {
            return; // Message d'erreur d√©j√† affich√© par getconnexion()
        }

        // ‚úÖ Cr√©ation du mod√®le et appel de la modification
        Sortie sortieModel = new Sortie(conn);
        String result = sortieModel.modifierSortie(idsortie, motif, montant, dateSQL.toString());

        // ‚úÖ Affichage du r√©sultat
        switch (result) {
            case "MODIFICATION_OK":
                Methodes.showSuccess("Sortie modifi√©e avec succ√®s !");
                break;
            case "SORTIE_NOT_FOUND":
                Methodes.showError("Sortie introuvable !");
                break;
            default:
                Methodes.showError("Erreur : " + result);
                break;
        }

    } catch (NumberFormatException ex) {
        Methodes.showError("Veuillez saisir un montant et un identifiant valides.");
    } catch (Exception ex) {
        ex.printStackTrace();
        Methodes.showError("Erreur inattendue : " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

